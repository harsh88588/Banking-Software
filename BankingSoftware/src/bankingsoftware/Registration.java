    private void MobileNumberBoxKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_MobileNumberBoxKeyTyped
        char c = evt.getKeyChar();
        if((Character.isDigit(c)) || (c==KeyEvent.VK_BACK_SPACE) || (c==KeyEvent.VK_DELETE)){
            MobileNumberBox.setEditable(true);
        }
        else{
            MobileNumberBox.setEditable(false);
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_MobileNumberBoxKeyTyped

