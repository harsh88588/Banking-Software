    private void CreateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateBtnActionPerformed
        String firstname = FirstNameBox.getText().toString();
        String lastname = LastNameBox.getText().toString();
        String username = UserNameBox.getText().toString();
        String email = EmailIdBox.getText().toString();
        String mobile = MobileNumberBox.getText().toString();
        String password = PasswordBox.getText().toString();
        String confpass = PasswordBox.getText().toString();
        int mob = mobile.length();
        
        
        if(firstname.equals("")|lastname.equals("")|username.equals("")|email.equals("")|mobile.equals("")|password.equals("")|confpass.equals("")){
            JOptionPane.showMessageDialog(null, "Please Fill All The Fields");
        }
        else if(mob!=10){
            JOptionPane.showMessageDialog(null, "Enter Valid Mobile Number");
        }
        else{
            try{
                String query = "INSERT INTO `registration`(`firstname`, `lastname`, `username`, `email`, `mobile`, `password`) VALUES (?,?,?,?,?,?)";
                conn = DriverManager.getConnection("jdbc:mysql://localhost/bankingproject","root","");
                pst = conn.prepareStatement(query);
                
                pst.setString(1, FirstNameBox.getText());
                pst.setString(2, LastNameBox.getText());
                pst.setString(3, UserNameBox.getText());
                pst.setString(4, EmailIdBox.getText());
                pst.setString(5, MobileNumberBox.getText());
                pst.setString(6, PasswordBox.getText());
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Registered Successfully");
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            
        }
        
        
    }//GEN-LAST:event_CreateBtnActionPerformed

